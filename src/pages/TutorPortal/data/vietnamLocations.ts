// Helper function to get districts for a province
export const getDistrictsForProvince = (provinceValue: string): District[] => {
    return VIETNAM_DISTRICTS[provinceValue] || [];
};

// Helper function to filter provinces by search text
export const filterProvinces = (searchText: string): Province[] => {
    if (!searchText) return VIETNAM_PROVINCES;
    const lowerSearch = searchText.toLowerCase();
    return VIETNAM_PROVINCES.filter(
        p => p.label.toLowerCase().includes(lowerSearch) ||
            p.value.toLowerCase().includes(lowerSearch)
    );
};

// Helper function to filter districts by search text
export const filterDistricts = (provinceValue: string, searchText: string): District[] => {
    const districts = getDistrictsForProvince(provinceValue);
    if (!searchText) return districts;
    const lowerSearch = searchText.toLowerCase();
    return districts.filter(
        d => d.label.toLowerCase().includes(lowerSearch) ||
            d.value.toLowerCase().includes(lowerSearch)
    );
};
